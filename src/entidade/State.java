package entidade;

/**
 *
 * @author minuzzi
 */
public class State {
    private int id;
    private String name,
                   uf;

    public State(String name, String uf) {
        this.name = name;
        this.uf = uf;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUf() {
        return uf;
    }

    public void setUf(String uf) {
        this.uf = uf;
    }
    
    
}
